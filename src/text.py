# -*- coding: utf-8 -*-
"""Предобработка текста для лексического поиска (BM25).

Пользователи пишут разговорно, с разными словоформами и опечатками. Приводим текст
к нормальной форме: чистим маск-токены, нижний регистр + ё→е, токенизация,
удаление функциональных стоп-слов, опциональный стемминг.

Замечания из анализа ошибок (Этап 1):
- в запросах есть маск-токены вида <MONEY>, <ID> — их надо удалять, иначе
  превращаются в мусорные токены money/id;
- Snowball-стемминг на этих данных скорее вредит, поэтому по умолчанию выключен.
"""
import re
from functools import lru_cache
from typing import List

# маск-плейсхолдеры в запросах: <MONEY>, <ID>, <PHONE>, <URL>, ... — удаляем целиком
_MASK_RE = re.compile(r"<[A-Za-z_]{1,20}>")
_TOKEN_RE = re.compile(r"[а-яёa-z0-9]+")

# компактный список русских функциональных стоп-слов (предлоги, союзы, местоимения,
# частицы). Доменные слова (доставка, возврат, профиль) НЕ включаем.
RU_STOPWORDS = set("""
и в во не что он на я с со как а то все она так его но да ты к у же вы за бы по
только ее мне было вот от меня еще нет о из ему теперь когда даже ну вдруг ли если
уже или ни быть был него до вас нибудь опять уж вам ведь там потом себя ничего ей
может они тут где есть надо ней для мы тебя их чем была сам чтоб без будто чего раз
тоже себе под будет ж тогда кто этот того потому этого какой совсем ним здесь этом
один почти мой тем чтобы нее сейчас были куда зачем всех можно при наконец два об
другой хоть после над больше тот через эти нас про всего них какая много разве три
эту моя впрочем хорошо свою этой перед иногда лучше чуть том нельзя такой им более
всегда конечно всю между это нам ваш наш свой ещё также этих эта здравствуйте
подскажите добрый день привет пожалуйста
""".split())


@lru_cache(maxsize=1)
def _get_stemmer():
    """Ленивая инициализация Snowball-стеммера (нужен только при stem=True)."""
    import snowballstemmer
    return snowballstemmer.stemmer("russian")


def tokenize(text: str, stem: bool = False, drop_stop: bool = True,
             min_len: int = 2) -> List[str]:
    """Текст -> список нормализованных токенов."""
    if not isinstance(text, str):
        return []
    text = _MASK_RE.sub(" ", text)          # убираем <MONEY>, <ID>, ...
    text = text.lower().replace("ё", "е")
    toks = _TOKEN_RE.findall(text)
    toks = [t for t in toks if len(t) >= min_len]
    if drop_stop:
        toks = [t for t in toks if t not in RU_STOPWORDS]
    if stem:
        toks = _get_stemmer().stemWords(toks)
    return toks
